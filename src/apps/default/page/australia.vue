<script>
import 'animate.css'
import 'swiper/dist/css/swiper.css'
import config from '../config'
import Swiper from 'swiper'
import swiperAnimate from 'assets/swiper.animate'
import musicfile from 'assets/imgs/the-old-you.mp3'

export default {
  ready() {
    this.$nextTick(()=>{
      // http://www.swiper.com.cn/usage/animate/index.html
      let swiper = new Swiper('.swiper-container', {
        direction: 'vertical',
        // 如果需要分页器
        pagination: '.swiper-pagination',
        // 如果需要滚动条
        // scrollbar: '.swiper-scrollbar',
        onInit(swiper) {
          //隐藏动画元素
          swiperAnimate.cache()
          //初始化完成开始动画
          swiperAnimate.animate(swiper, true) 
        },
        onSlideChangeEnd(swiper) {
          //每个slide切换结束时也运行当前slide动画
          swiperAnimate.animate(swiper, true) 
        }
      })

      this.createAudio()
      this.playAudio()
    })
  },
  destroyed() {
    this.playAudio()
  },
  methods: {
    playAudio(isPlay = true) {
      let audio = document.getElementById('audio')
      let audioBtn = document.getElementById('audio-btn')
      if(audio){
        if(audio.paused){
          if(isPlay){
            audio.play() 
            audioBtn.className = 'l-spin'
          }
        }else{
          audio.pause()
          audioBtn.className = ''
        }
      }
    },
    createAudio() {
      let audioBtn = document.getElementById('audio-btn')
      let audio = document.getElementById('audio')
      if(!audio){
        audio = document.createElement('audio')
        audio.id = 'audio'
        audio.style.display = 'none'
        audio.loop = true
        audio.autoplay = false
        audio.volume = 0.8
        audio.src = musicfile
        document.body.appendChild(audio)
        document.addEventListener('touchstart', function touch(){ 
        document.removeEventListener('touchstart', touch)
          audioBtn.className = 'l-spin'
          audio.paused && audio.play()
        }, false)
      }

      audio.load()
      // audio.play()
    }
  }
}
