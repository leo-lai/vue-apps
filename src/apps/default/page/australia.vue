import config from '../config'
import Swiper from 'iswiper'

const musicfile = require('assets/imgs/blue-lotus.mp3')
function createAudio(){
  let audio = document.createElement('audio')
  document.body.appendChild(audio)
  audio.id = 'audio'
  audio.style.display = 'none'
  audio.loop = true
  audio.autoplay = true
  audio.volume = 0.8

  if(audio.addEventListener){
    audio.addEventListener('loadeddata',function(e){  
      document.getElementById('audio-btn').className = 'l-spin'
    })
  }
  audio.src = musicfile
  // audio.play()
}

export default {
  components: {
  },
  events: {
    'hook:ready': function() {
      let self = this
      self.$nextTick(()=>{
        setTimeout(()=>{
          let swiper = new Swiper({
            container: '.l-swiper',
            item: '.l-swiper-item',
            direction: 'vertical',
            activeClass: 'l-swiper-active'
          })
          createAudio()
        }, 1000)
      })
    },
    'hook:beforeDestroy': function() {
      this.playAudio()
    }
  },
  data() {
    return {

    }
  },
  methods: {
    playAudio() {
      let audio = document.getElementById('audio')
      let audioBtn = document.getElementById('audio-btn')
      if(audio){
        if(audio.paused){
          audio.play()
          audioBtn.className = 'l-spin'
        }else{
          audio.pause()
          audioBtn.className = ''
        }
      }
    }
  }
}
